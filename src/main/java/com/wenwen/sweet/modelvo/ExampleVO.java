public class ExampleVO {

    public String val;
    public String trans;

    public ExampleVO() {
    }

    public ExampleVO(String val, String trans) {
        this.val = val;
        this.trans = trans;
    }

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }

    public String getTrans() {
        return trans;
    }

    public void setTrans(String trans) {
        this.trans = trans;
    }
}
